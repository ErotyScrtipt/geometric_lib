def area(a, h): 
    '''
        Считает площадь треугольника по заданным сторонам
        :param a: длина стороны треугольника
        :param h: высота треугольника
        :type a: float
        :type h: float
        :return: площадь треугольника
        :rtype: float
    '''
    return a * h / 2 

def perimeter(a, b, c): 
    '''
        Считает периметр треугольника по заданным сторонам
        :param a: длина стороны треугольника
        :param b: длина стороны треугольника
        :param c: длина стороны треугольника
        :type a: float
        :type b: float
        :type c: float
        :return: периметр треугольника
        :rtype: float
    '''
    return a + b + c 

