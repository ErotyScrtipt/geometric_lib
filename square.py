
def area(a):
    '''
        Считает площадь квадрата
        :param a: длина стороны квадрата
        :type a: float
        :return: площадь квадрата
        :rtype: float
    '''
    return a * a


def perimeter(a):
    '''
        Считает периметр квадрата
        :param a: длина стороны квадрата
        :type a: float
        :return: периметр квадрата
        :rtype: float
    '''
    return 4 * a
