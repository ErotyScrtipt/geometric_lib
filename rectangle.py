def area(a, b): 
    '''
        Считает площадь прямоугольника по заданным сторонам
        :param a: длина прямоугольника
        :param b: ширина прямоугольника
        :type a: float
        :type b: float
        :return: площадь прямоугольника
        :rtype: float
    '''
    return a * b 

def perimeter(a, b): 
    '''
        Считает периметр прямоугольника по заданным сторонам
        :param a: длина прямоугольника
        :param b: ширина прямоугольника 
        :type a: float
        :type b: float 
        :return: периметр прямоугольника
        :rtype: float
    '''
    return (a + b) * 2